    private void carregarCategorias() {
        List<Categoria> lista = CategoriaDAO.getCategorias();
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        Categoria s = new Categoria(0, "Selecione...");
        model.addElement(s);
        for (Categoria c : lista) {
            model.addElement(c);
        }
        cmbCategoria.setModel(model);
    }

    private void carregarFormulario(int _id) {
        produto = ProdutoDAO.getProdutosById(_id);

        lblCodigo.setText(String.valueOf(produto.getId()));
        txtNome.setText(produto.getNome());
        txtCodigoBarras.setText(produto.getCodigoBarras());
                
        String preco = String.valueOf(produto.getPreco());
        preco = preco.replace(".", ",");
        txtPreco.setText( preco );
        
        String quant = String.valueOf(produto.getQuantidade());
        preco = preco.replace(".", ",");
        txtQuantidade.setText( quant );

        DefaultComboBoxModel modelCategorias
                = (DefaultComboBoxModel) cmbCategoria.getModel();

        for (int i = 1; i < modelCategorias.getSize(); i++) {
            Categoria proModel = (Categoria) modelCategorias.getElementAt(i);
            if (proModel.getId() == produto.getCategoria().getId()) {
                cmbCategoria.setSelectedIndex(i);
                break;
            }
        }

    }

