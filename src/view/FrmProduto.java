    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String nome = txtNome.getText();
        Categoria cat = (Categoria) cmbCategoria.getSelectedItem();
        if (nome.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Você deve informar o nome!");
        } else {
            if (cat.getId() == 0) {
                JOptionPane.showMessageDialog(this,
                        "Você deve selecionar uma categoria!");
            } else {
                boolean novo = false;
                if( produto == null ){
                    novo = true;
                    produto = new Produto();
                }
                    
                produto.setNome(nome);
                produto.setCodigoBarras(txtCodigoBarras.getText());
                
                String preco = txtPreco.getText().replace(",", ".");
                produto.setPreco(Double.valueOf(preco));
                
             
                
                String quantidade = txtQuantidade.getText().replace(",", ".");
                produto.setQuantidade(Double.valueOf(quantidade));
              
                produto.setCategoria(cat);
                
                

                ProdutoDAO dao = new ProdutoDAO();
                if( novo ){
                    dao.inserir(produto);
                    produto = null;
                }else{
                    dao.editar(produto);
                    telaListProduto.carregarTabela();
                    this.dispose();
                }
                

                limpar();
            }
        
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

